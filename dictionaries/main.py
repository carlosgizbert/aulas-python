items = {
  "Guilherme" : 1,
  "cachorro" : 2,
  "nome" : 2,
  "vindo" : 1
}

print('---------------')

# percorre os items
for elemento in items.items():
    print(elemento) 

print('---------------')

# percorre os elementos
for elemento in items:
    print(elemento) 

print('---------------')

# percorre os valores
for elemento in items.values():
    print(elemento) 